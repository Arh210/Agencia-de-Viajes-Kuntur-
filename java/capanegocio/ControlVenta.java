package capanegocio;


public class ControlVenta {
    //atributos de detalle de control de venta
    private String cantidadVentas;
    private String duracion;
    private String paquete;

    public String getCantidadVentas() {
        return cantidadVentas;
    }

    public void setCantidadVentas(String cantidadVentas) {
        this.cantidadVentas = cantidadVentas;
    }

    public String getDuracion() {
        return duracion;
    }

    public void setDuracion(String duración) {
        this.duracion = duración;
    }

    public String getPaquete() {
        return paquete;
    }

    public void setPaquete(String paquete) {
        this.paquete = paquete;
    }



}
